# ================================================
# RUBY->CONTROLLER->PAGES-CONTROLLER =============
# ================================================
class PagesController < ApplicationController

  # ----------------------------------------------
  # LAYOUT ---------------------------------------
  # ----------------------------------------------
  layout "market"

  # ==============================================
  # ACTIONS ======================================
  # ==============================================

  # ----------------------------------------------
  # SHOW -----------------------------------------
  # ----------------------------------------------
  def show
    if valid_page?
      render template: "pages/#{params[:page]}"
    end
  end

  # ==============================================
  # PRIVATE ======================================
  # ==============================================
  private

  # ----------------------------------------------
  # VALID-PAGE -----------------------------------
  # ----------------------------------------------
  def valid_page?
    File.exist?(Pathname.new(Rails.root + "app/views/pages/#{params[:page]}.html.erb"))
  end

end
