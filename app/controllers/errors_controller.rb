class ErrorsController < ApplicationController

  # ==============================================
  # ACTIONS ======================================
  # ==============================================

  # ----------------------------------------------
  # NOT-FOUND ------------------------------------
  # ----------------------------------------------
  def not_found
    render(:status => 404)
  end

  # ----------------------------------------------
  # INTERNAL-SERVER-ERROR ------------------------
  # ----------------------------------------------
  def internal_server_error
    render(:status => 500)
  end

end
