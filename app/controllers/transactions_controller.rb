# ================================================
# RUBY->CONTROLLER->TRANSACTIONS-CONTROLLER ======
# ================================================
class TransactionsController < ApplicationController

  # ----------------------------------------------
  # FILTERS --------------------------------------
  # ----------------------------------------------
  before_action :set_transaction, only: [:show, :update, :edit]

  # ==============================================
  # ACTIONS ======================================
  # ==============================================

  # ----------------------------------------------
  # INDEX ----------------------------------------
  # ----------------------------------------------
  def index
    @user = User.find(current_user.id)
    @transactions = @user.transactions
  end

  # ----------------------------------------------
  # SHOW -----------------------------------------
  # ----------------------------------------------
  def show
  end

  # ==============================================
  # PRIVATE ======================================
  # ==============================================
  private

  # ----------------------------------------------
  # SET-TRANSACTIONS -----------------------------
  # ----------------------------------------------
  def set_transactions
    @transaction = Transaction.where(plaid_trans_id: params[:plaid_trans_id])
  end

  # ----------------------------------------------
  # TRANSACTION-PARAMS ---------------------------
  # ----------------------------------------------
  def transaciton_params
    params.require(:plaid_trans_id).permit(:plaid_trans_id)
  end
end
